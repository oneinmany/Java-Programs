package Multipleobj;
public class Main {
  // Fields of the class
  int X = 100;
  String S = "Oneinmany";

  public static void main(String[] args) {
      // Create an instance of Main
      Main myObj = new Main();
      // Print fields to demonstrate usage
      System.out.println(myObj.X);
      System.out.println(myObj.S);
  }
}

